// Helper function to get crops by category
export const getCropsByCategory = (category: CropData['category']): CropData[] => {
  return cropDatabase.filter(crop => crop.category === category);
};

// Helper function to get crops suitable for a region
export const getCropsByRegion = (region: string): CropData[] => {
  return cropDatabase.filter(crop => 
    crop.regions.some(r => r.toLowerCase().includes(region.toLowerCase()))
  );
};

